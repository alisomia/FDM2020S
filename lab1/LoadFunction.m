function [U,U0,F,g,Basis] = LoadFunction(Probid, Basisid, N)
u{1} = @(x,y) exp((x.^2+y.^2)/2);
f{1} = @(x,y) (1+x.^2+y.^2).*exp((x.^2+y.^2));
u{2} = @(x,y) 0.5*(max(sqrt((x-0.5).^2+(y-0.5).^2)-0.2,0)).^2;
f{2} = @(x,y)max(1-0.2./sqrt((x-0.5).^2+(y-0.5).^2),0);
u{3}= @(x,y) -sqrt(2-(x.^2+y.^2));
f{3} = @(x,y) 2./(2-(x.^2+y.^2)).^2;
init = @(x,y) 0.5*(x.^2+y.^2) ;

u = u{Probid};
f = f{Probid};

g.l  = @(x) u(x,0).*(x>= eps).*(x<=1+eps);
g.r = @(x) u(x,1).*(x>=-eps).*(x<=1-eps);
g.u = @(x) u(1,x).*(x>=eps).*(x<=1+eps);
g.d = @(x) u(0,x).*(x>=-eps).*(x<=1-eps);


X = (1:N-1)/N;
Y = X';
U = init(X,Y);
U0 = u(X,Y);
F = f(X,Y);
Basis{1} = {{[1,0],[0,1]},{[1,1],[-1,1]}}; 
Basis{2} = {{[1,0],[0,1]},{[1,1],[-1,1]}, {[2,1],[-1,2]}, {[1,2],[-2,1]}}; 
Basis{3} = {{[1,0],[0,1]},{[1,1],[-1,1]}, {[2,1],[-1,2]}, {[1,2],[-2,1]}, {[3,1],[-1,3]}, {[1,3],[-3,1]}, {[3,2],[-2,3]}, {[2,3],[-3,2]}};
Basis = Basis{Basisid};
end